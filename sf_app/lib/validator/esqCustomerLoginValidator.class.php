class esqCustomerLoginValidator extends sfValidatorBase
{
  public function configure($options = array(), $messages = array())
  {
    $this->addOption('username_field', 'username');
    $this->addOption('password_field', 'password');
    $this->addOption('throw_global_error', false);

    $this->setMessage('invalid', 'The username and/or password is invalid.');
  }

  protected function doClean($values)
  {
    $username = isset($values[$this->getOption('username_field')]) ? $values[$this->getOption('username_field')] : '';
    $password = isset($values[$this->getOption('password_field')]) ? $values[$this->getOption('password_field')] : '';

    // customer exists? we're allowing suspended customers to login as well
    if ($customer = CustomerQuery::create() ->
      filterByType(Customer::TYPE_SHOPPING, Criteria::NOT_EQUAL) ->
      filterByStatus(Customer::STATUS_CANCELLED, Criteria::NOT_EQUAL) ->
      findOneByUsername($username))
    {
      // password is ok?
      if ($customer->checkPassword($password))
      {
        return array_merge($values, array('customer' => $customer));
      }
    }

    if ($this->getOption('throw_global_error'))
    {
      throw new sfValidatorError($this, 'invalid');
    }

    throw new sfValidatorErrorSchema($this, array($this->getOption('username_field') => new sfValidatorError($this, 'invalid')));
  }
}
